package com.gronglegrowth.correspondentcommerce;

import net.minecraft.client.item.TooltipContext;
import net.minecraft.item.Item;
import net.minecraft.item.ItemStack;
import net.minecraft.text.Text;
import net.minecraft.util.Formatting;
import net.minecraft.world.World;

import java.util.List;

public class ExchangeStoneItem extends Item {
    public ExchangeStoneItem(Settings settings) {
        super(settings);
    }

    @Override
    public void appendTooltip(ItemStack itemStack, World world, List<Text> tooltip, TooltipContext tooltipContext) {
        tooltip.add(Text.translatable("item.correspondentcommerce.rubedo_stone.tooltip").formatted(Formatting.GRAY));

    }
}
